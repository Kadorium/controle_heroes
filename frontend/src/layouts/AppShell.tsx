export function AppShell() {
  const { user, logout } = useAuth();
  const navigate = useNavigate();

  if (!user) return null;

  const initials = user.name
    .split(" ")
    .map((w: string) => w[0])
    .join("")
    .toUpperCase()
    .slice(0, 2);

  return (
    <div className="app-shell--full">
      <header className="topbar">
        <div className="topbar__left">
          <span className="topbar__logo">Epic</span>
          <nav className="topbar__nav">
            {NAV_ITEMS.map((item) => (
              <NavLink
                key={item.to}
                to={item.to}
                end={item.end}
                className={({ isActive }) =>
                  `topbar__link${isActive ? " topbar__link--active" : ""}`
                }
              >
                {item.icon}
                <span>{item.label}</span>
              </NavLink>
            ))}
          </nav>
        </div>

        <div className="topbar__right">
          {/* Placeholder: busca */}
          <button className="topbar__icon-btn" title="Buscar" type="button">
            <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
              <circle cx="11" cy="11" r="8" />
              <line x1="21" y1="21" x2="16.65" y2="16.65" />
            </svg>
          </button>

          {/* Placeholder: notificações */}
          <button className="topbar__icon-btn topbar__icon-btn--notif" title="Notificações" type="button">
            <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
              <path d="M18 8A6 6 0 0 0 6 8c0 7-3 9-3 9h18s-3-2-3-9" />
              <path d="M13.73 21a2 2 0 0 1-3.46 0" />
            </svg>
            <span className="topbar__notif-dot" />
          </button>

          {/* Avatar + user info */}
          <div className="topbar__user">
            <div className="topbar__avatar">{initials}</div>
            <div className="topbar__user-info">
              <span className="topbar__user-name">{user.name}</span>
              <span className="topbar__user-role">{user.role}</span>
            </div>
          </div>

          <button
            className="topbar__icon-btn"
            title="Sair"
            type="button"
            onClick={async () => {
              await logout();
              navigate("/login", { replace: true });
            }}
          >
            <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
              <path d="M9 21H5a2 2 0 0 1-2-2V5a2 2 0 0 1 2-2h4" />
              <polyline points="16 17 21 12 16 7" />
              <line x1="21" y1="12" x2="9" y2="12" />
            </svg>
          </button>
        </div>
      </header>

      <main className="app-main">
        <Outlet />
      </main>
    </div>
  );
}
